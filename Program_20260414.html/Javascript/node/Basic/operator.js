// declare 2 numeric variabbles
let x = 10;
let y = 5;

console.log("............. Numeric Operators.............");
// using numeric operators
console.log("The sum of " + x + " and " + y + " is " + (x + y));
console.log("The difference between " + x + " and " + y + " is " + (x - y));
console.log("The product of " + x + " and " + y + " is " + x * y);
console.log("The division of " + x + " and " + y + " is " + x / y);
console.log("The remainder of " + x + " and " + y + " is " + (x % y));

console.log("............. Assignment Operators.............");
// assignment operators
console.log("The current value of x is " + x);
console.log("The value after adding 10 is " + (x += 10));
console.log("The new value of x now is " + x);
console.log("The value after substracting 5 is " + (x -= 5));
console.log("The value after multiplication by 2 is " + (x *= 2));
console.log("The value after dividing by 3 is " + (x /= 3));
console.log("The remainder after dividing by 3 is " + (x %= 3));

console.log("............. Comparison Operators.............");
// Comparison operators
console.log("The value of x is " + x);
console.log("The value of y is " + y);
console.log("x is not equal to y: " + (x != y));
console.log("x is equal to y: " + (x == y));
console.log("x is strictly equal to y: " + (x === y));
console.log("x is strictly not equal to y: " + (x !== y));
console.log("x is greater than y: " + (x > y));
console.log("x is less than y: " + (x < y));
console.log("x is greater than or equal to y: " + (x >= y));
console.log("x is less than or equal to y: " + (x <= y));

console.log("............. Increment and Decrement Operators.............");
// Increment and Decrement operators
let a = 10;
console.log("The value of a is : " + a); // 10
console.log("The value of a after post-increment is : " + a++); // 10
console.log("The value of a is : " + a); // 11
console.log("The value of a after pre-increment is : " + ++a); //12
console.log("The value of a is : " + a); // 12
console.log("The value of a after post-decrement is : " + a--); //11
console.log("The value of a is : " + a); // 11
console.log("The value of a after pre-decrement is : " + --a); //10
console.log("The final value of a is : " + a); // 10

